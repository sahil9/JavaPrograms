package academy.learnprogramming;

public class Dimensions {
    private int lenght;
    private int height;

    public Dimensions(int lenght, int height) {
        this.lenght = lenght;
        this.height = height;
    }

    public int getLenght() {
        return lenght;
    }

    public int getHeight() {
        return height;
    }
}
